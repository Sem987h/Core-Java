
public class DataTypeDamo
{
	public static void main(String[] args)
	{
		int i1=10;// 4 byte
		long l1=123;// 8 byte
		byte b1=127;// 1 byte
		float f1=12.21f;// 4 byte
		double d1=6436543.32;// 8 byte
		char c1='A';// 2 byte
		boolean b=true;//1 bit
		
		System.out.println("Int is.. "+i1);
		System.out.println("Long is.. "+l1);
		System.out.println("Byte is.. "+b1);
		System.out.println("Float is.. "+f1);
		System.out.println("Double is.. "+d1);
		System.out.println("Char is.. "+c1);
		System.out.println("Boolean is.. "+b1);
	}
}
